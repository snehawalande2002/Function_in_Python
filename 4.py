def add(a,b):
    print("The addition of two values is: ")
    c= a+b
    print(c)

def sub(x,y):
    print("The subtraction is: ")
    z=x-y
    print(z)

def mul(m,n):
    print("The multiplication is: ")
    o=m*n
    print(o)

def div(i,j):
    print("The division is: ")
    k=i/j
    print(k)

add(20,10)
sub(20,10)
mul(2,3)
div(9,3)